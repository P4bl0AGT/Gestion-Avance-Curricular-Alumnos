    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField1.getText();
        String apellido = jTextField2.getText();
        int edad = Integer.parseInt(jTextField3.getText());
        String rut = jTextField4.getText();
        int indice = Integer.parseInt(jTextField5.getText());
        Carrera carrera = instituto.obtenerCarrera(indice);
        Alumno nuevoAlumno = new Alumno(nombre, apellido, rut, edad, carrera);
        instituto.agregarAlumno(nuevoAlumno);
        jTextArea1.setText("Alumnos ingresado Exitosamente");
        jTextField1.setText(" ");
        jTextField2.setText(" ");
        jTextField3.setText(" ");
        jTextField4.setText(" ");
        jTextField5.setText(" ");
    }//GEN-LAST:event_jButton2ActionPerformed

