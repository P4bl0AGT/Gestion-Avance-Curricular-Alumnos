    private void mostrarCarreras()
    {
        jTextArea1.setEditable(false);
        int talla = instituto.cantidadCarreras();
        for (int i = 0; i < talla; i++) {
            jTextArea1.append(i + ".- " + instituto.obtenerCarrera(i).getNombre() + "\n");
        }
    }
    
